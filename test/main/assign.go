package main

func main(){
	a := 5+1
	b := 1+1
	println(a)
	println(b)
	
	a++
	println(a)
	println(b)

	a--
	println(a)
	println(b)

	a += 2+1
	b += 3+1
	println(a)
	println(b)

	a -= 3+1
	b -= 7+1
	println(a)
	println(b)

	a *= 4+1
	b *= -4+1
	println(a)
	println(b)

	a /= 5+1
	b /= -6+1
	println(a)
	println(b)

	a %= 6+1
	b %= -7+1
	println(a)
	println(b)

	a ^= 7+1
	b ^= -8+1
	println(a)
	println(b)

	a &= 9+1
	b &= -10+1
	println(a)
	println(b)

	a |= 10+1
	b |= -11+1
	println(a)
	println(b)

	a <<= 11+1
	b <<= 3+1
	println(a)
	println(b)

	a >>= 12+1
	b >>= 4+1
	println(a)
	println(b)

	a &^= 13+1
	b &^= -14+1
	println(a)
	println(b)

}
