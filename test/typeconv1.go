package main

// Test type conversion and inference of int literals

func main() {

	a := byte(1)
	b := uint8(2)
	c := int8(3)

	d := int16(4)
	e := uint16(5)

	f := int32(4)
	g := uint32(5)

	h := int(6)
	i := uint(7)

	j := int64(6)
	k := uint64(7)

	println(a)
	println(b)
	println(c)
	println(d)
	println(e)
	println(f)
	println(g)
	println(h)
	println(i)
	println(j)
	println(k)
}
